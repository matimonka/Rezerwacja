		<h1 style= "text-align:center;">REGULAMIN KORZYSTANIA Z SIŁOWNI I OBIEKTÓW SPORTOWYCH</h1>
<h2 style="text-align:center;">I. ZASADY OGÓLNE</h2>

1. Siłownia oraz obiekty sportowe są czynne całą dobe od poniedziałku do niedzieli.<br>
2. Użytkownikami siłowni oraz obiektów sportowych mogą być wyłącznie osoby, które zaakceptowały postawienia
niniejszego regulaminu oraz zobowiązały się do jego stosowania.<br>
3. Pracownicy obsługi Siłowni mają prawo wydawać użytkownikom wytyczne/polecenia o 
charakterze porządkowym i organizacyjnym. Nie zastosowanie się do poleceń/wytycznych 
będzie traktowane jako nieprzestrzeganie postanowień niniejszego regulaminu.<br>
4. Użytkownicy zobowiązani są do bezwzględnego podporządkowania się komunikatom oraz
sygnałom o charakterze ostrzegawczym lub informacyjnym.<br>
5. Zabrania się prowadzenia na terenie obiektu jakiejkolwiek działalności zarobkowej, a w 
szczególności działalności handlowej, marketingowej i usługowej, rozprowadzania ulotek, 
sprzedaży towarów, oferowania i wykonywania usług, w tym prywatnych zajęć
indywidualnych za wynagrodzeniem.<br>
6. Właściciel obiektów nie ponosi odpowiedzialności za wypadki i zdarzenia 
spowodowane nie przestrzeganiem postanowień niniejszego regulaminu, a także powstałe 
wskutek winy użytkowników.<br>
7. Użytkownicy obiektów odpowiadają na zasadach ogólnych za wszelkie szkody wyrządzone 
ich działaniem lub zaniechaniem.<br>
<h2>II. ZASADY KORZYSTANIA Z OBIEKTÓW</h2>
1. Z zastrzeżeniem pkt. 2, z Obiektów mogą korzystać wyłącznie osoby pełnoletnie.<br>
2. Młodzież w wieku 16-17 lat, może korzystać z obiektów wyłącznie po wyrażeniu zgody 
przez rodziców/opiekunów prawnych . Warunkiem dopuszczenia do zajęć jest złożenie
oświadczenia woli stanowiącego załącznik do niniejszego regulaminu, pod nadzorem osoby 
pełnoletniej, która złoży oświadczenie o odpowiedzialności za osoby niepełnoletnie.<br>
3. Użytkownicy Siłowni zobligowani są posiadać:
a) strój sportowy,
b) zmienne obuwie sportowe,
c) duży ręcznik, przeznaczony wyłącznie do treningu, który ze względów higienicznych przed
rozpoczęciem ćwiczeń należy rozkładać na urządzeniach.<br>
4. Po zakończeniu ćwiczenia użytkownik zobowiązany jest odłożyć urządzenia/sprzęt na 
miejsce jego usytuowania.<br>
5. Z obiektów mogą korzystać wyłącznie Użytkownicy, których stan zdrowia pozwala na
wykonywanie ćwiczeń fizycznych. Użytkownicy wykonują ćwiczenia wyłącznie na własne 
ryzyko i własną odpowiedzialność.<br>
6. Każda zgłoszona grupa ćwiczących na dany dzień i godzinę wg harmonogramu wyznacza 
spośród siebie osobę odpowiedzialną, która potwierdza podpisem przyjęcie obszaru Siłowni 
do ćwiczeń pracownikowi obsługi Obiektu.<br>
7. Na terenie Siłowni zabrania się:
a) biegania (nie dotyczy korzystania z sali),
b) używania urządzeń oraz sprzętu treningowego w sposób niezgodny z ich przeznaczeniem,
c) wnoszenia urządzeń własnych, jako treningowych,
d) wnoszenia przedmiotów stanowiących zagrożenie dla zdrowia i życia użytkowników (tj. w 
szczególności ostrych narzędzi, szklanych opakowań, puszek etc.),
e) wstępu i przebywania dla osób, znajdujących się w stanie wskazującym na spożycie 
napojów alkoholowych lub będących pod wpływem substancji odurzających i 
psychotropowych,
f) wszczynania fałszywych alarmów,
g) niszczenia wszelkich rzeczy stanowiących wyposażenia Siłowni,
h) zaśmiecania i zanieczyszczania pomieszczeń,
i) palenia tytoniu,
j) wprowadzania zwierząt,
k) wnoszenia przedmiotów utrudniających komunikację w obiekcie.<br>
<h2>III. POSTANOWIENIA KOŃCOWE</h2>
1. Użytkownicy nie przestrzegający postanowień niniejszego regulaminu oraz
obowiązujących na terenie obiektu przepisów porządkowych, zostaną wyproszeni z Siłowni.<br>
2. Skargi i wnioski można składać bezpośrednio u pracowników obiektów oraz za pomocą formularza kontaktowego.<br>



